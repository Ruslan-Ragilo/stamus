            <p>2) несовершеннолетнего больного наркоманией при оказании ему наркологической помощи или при медицинском освидетельствовании несовершеннолетнего в целях установления состояния наркотического либо иного токсического опьянения (за исключением установленных законодательством Российской Федерации случаев приобретения несовершеннолетними полной дееспособности до достижения ими восемнадцатилетнего возраста).</p>
            <p>1. Гражданин, один из родителей или иной законный представитель лица, указанного в части 2 настоящей статьи, имеют право отказаться от медицинского вмешательства или потребовать его прекращения, за исключением случаев, предусмотренных частью 9 настоящей статьи. Законный представитель лица, признанного в установленном законом порядке недееспособным, осуществляет указанное право в случае, если такое лицо по своему состоянию не способно отказаться от медицинского вмешательства.</p>
            <p>2. При отказе от медицинского вмешательства гражданину, одному из родителей или иному законному представителю лица, указанного в части 2 настоящей статьи, в доступной для него форме должны быть разъяснены возможные последствия такого отказа.</p>
            <p>3. При отказе одного из родителей или иного законного представителя лица, указанного в части 2 настоящей статьи, либо законного представителя лица, признанного в установленном законом порядке недееспособным, от медицинского вмешательства, необходимого для спасения его жизни, медицинская организация имеет право обратиться в суд для защиты интересов такого лица. Законный представитель лица, признанного в установленном законом порядке недееспособным, извещает орган опеки и попечительства по месту жительства подопечного об отказе от медицинского вмешательства, необходимого для спасения жизни подопечного, не позднее дня, следующего за днем этого отказа.</p>
            <p>4. Лица, указанные в частях 1 и 2 настоящей статьи, для получения первичной медико-санитарной помощи при выборе врача и медицинской организации на срок их выбора дают информированное добровольное согласие на определенные виды медицинского вмешательства, которые включаются в перечень, устанавливаемый уполномоченным федеральным органом исполнительной власти.</p>
            <p>5. Информированное добровольное согласие на медицинское вмешательство или отказ от медицинского вмешательства оформляется в письменной форме, подписывается гражданином, одним из родителей или иным законным представителем, медицинским работником и содержится в медицинской документации пациента.</p>
            <p>6. Порядок дачи информированного добровольного согласия на медицинское вмешательство и отказа от медицинского вмешательства, в том числе в отношении определенных видов медицинского вмешательства, форма информированного добровольного согласия на медицинское вмешательство и форма отказа от медицинского вмешательства утверждаются уполномоченным федеральным органом исполнительной власти. (в ред. Федерального закона от 25.11.2013 N 317-ФЗ)</p>
            <p>1. Медицинское вмешательство без согласия гражданина, одного из родителей или иного законного представителя допускается:</p>
            <p>1) если медицинское вмешательство необходимо по экстренным показаниям для устранения угрозы жизни человека и если его состояние не позволяет выразить свою волю или отсутствуют законные представители (в отношении лиц, указанных в части 2 настоящей статьи);</p>
            <p>2) в отношении лиц, страдающих заболеваниями, представляющими опасность для окружающих;</p>
            <p>3) в отношении лиц, страдающих тяжелыми психическими расстройствами;</p>
            <p>4) в отношении лиц, совершивших общественно опасные деяния (преступления);</p>
            <p>5) при проведении судебно-медицинской экспертизы и (или) судебно-психиатрической экспертизы.</p>
            <p>1. Решение о медицинском вмешательстве без согласия гражданина, одного из родителей или иного законного представителя принимается:</p>
            <p>1) в случаях, указанных в пунктах 1 и 2 части 9 настоящей статьи, — консилиумом врачей, а в случае, если собрать консилиум невозможно, — непосредственно лечащим (дежурным) врачом с внесением такого решения в медицинскую документацию пациента и последующим уведомлением должностных лиц медицинской организации (руководителя медицинской организации или руководителя отделения медицинской организации), гражданина, в отношении которого проведено медицинское вмешательство, одного из родителей или иного законного представителя лица, которое указано в части 2 настоящей статьи и в отношении которого проведено медицинское вмешательство, либо судом в случаях и в порядке, которые установлены законодательством Российской Федерации; (в ред. Федерального закона от 25.11.2013 N 317-ФЗ)</p>
            <p>2) в отношении лиц, указанных в пунктах 3 и 4 части 9 настоящей статьи, — судом в случаях и в порядке, которые установлены законодательством Российской Федерации.</p>
            <p>1. К лицам, совершившим преступления, могут быть применены принудительные меры медицинского характера по основаниям и в порядке, которые установлены федеральным законом. Статья 21. Выбор врача и медицинской организации</p>
            <p>1. При оказании гражданину медицинской помощи в рамках программы государственных гарантий бесплатного оказания гражданам медицинской помощи он имеет право на выбор медицинской организации в порядке, утвержденном уполномоченным федеральным органом исполнительной власти, и на выбор врача с учетом согласия врача. Особенности выбора медицинской организации гражданами, проживающими в закрытых административно-территориальных образованиях, на территориях с опасными для здоровья человека физическими, химическими и биологическими факторами, включенных в соответствующий перечень, а также работниками организаций, включенных в перечень организаций отдельных отраслей промышленности с особо опасными условиями труда, устанавливаются Правительством Российской Федерации.</p>
            <p>2. Для получения первичной медико-санитарной помощи гражданин выбирает медицинскую организацию, в том числе по территориально-участковому принципу, не чаще чем один раз в год (за исключением случаев изменения места жительства или места пребывания гражданина). В выбранной медицинской организации гражданин осуществляет выбор не чаще чем один раз в год (за исключением случаев замены медицинской организации) врача-терапевта, врача-терапевта участкового, врача-педиатра, врача-педиатра участкового, врача общей практики (семейного врача) или фельдшера путем подачи заявления лично или через своего представителя на имя руководителя медицинской организации.</p>
            <p>3. Оказание первичной специализированной медико-санитарной помощи осуществляется:</p>
            <p>1) по направлению врача-терапевта участкового, врача-педиатра участкового, врача общей практики (семейного врача), фельдшера, врача-специалиста;</p>
            <p>2) в случае самостоятельного обращения гражданина в медицинскую организацию, в том числе организацию, выбранную им в соответствии с частью 2 настоящей статьи, с учетом порядков оказания медицинской помощи.</p>
