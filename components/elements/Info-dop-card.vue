            <p>2. Медицинская помощь в неотложной или экстренной форме оказывается гражданам с учетом соблюдения установленных требований к срокам ее оказания.</p>
            <p>3. При оказании гражданину медицинской помощи в рамках программы государственных гарантий бесплатного оказания гражданам медицинской помощи выбор медицинской организации (за исключением случаев оказания скорой медицинской помощи) за пределами территории субъекта Российской Федерации, в котором проживает гражданин, осуществляется в порядке, устанавливаемом уполномоченным федеральным органом исполнительной власти.</p>
            <p>4. При выборе врача и медицинской организации гражданин имеет право на получение информации в доступной для него форме, в том числе размещенной в информационно-телекоммуникационной сети «Интернет» (далее — сеть «Интернет»), о медицинской организации, об осуществляемой ею медицинской деятельности и о врачах, об уровне их образования и квалификации.</p>
            <p>5. Выбор врача и медицинской организации военнослужащими и лицами, приравненными по медицинскому обеспечению к военнослужащим, гражданами, проходящими альтернативную гражданскую службу, гражданами, подлежащими призыву на военную службу или направляемыми на альтернативную гражданскую службу, и гражданами, поступающими на военную службу по контракту или приравненную к ней службу, а также задержанными, заключенными под стражу, отбывающими наказание в виде ограничения свободы, ареста, лишения свободы либо административного ареста осуществляется с учетом особенностей оказания медицинской помощи, установленных статьями 25 и 26 настоящего Федерального закона.</p>
            <p>6. При оказании гражданам медицинской помощи в рамках практической подготовки обучающихся по профессиональным образовательным программам медицинского образования пациент должен быть проинформирован об участии обучающихся в оказании ему медицинской помощи и вправе отказаться от участия обучающихся в оказании ему медицинской помощи. В этом случае медицинская организация обязана оказать такому пациенту медицинскую помощь без участия обучающихся. (часть 9 введена Федеральным законом от 02.07.2013 N 185-ФЗ)</p>
            <p>Статья 22. Информация о состоянии здоровья</p>
            <p>1. Каждый имеет право получить в доступной для него форме имеющуюся в медицинской организации информацию о состоянии своего здоровья, в том числе сведения о результатах медицинского обследования, наличии заболевания, об установленном диагнозе и о прогнозе развития заболевания, методах оказания медицинской помощи, связанном с ними риске, возможных видах медицинского вмешательства, его последствиях и результатах оказания медицинской помощи.</p>
            <p>2. Информация о состоянии здоровья предоставляется пациенту лично лечащим врачом или другими медицинскими работниками, принимающими непосредственное участие в медицинском обследовании и лечении. В отношении лиц, не достигших возраста, установленного в части 2 статьи 54 настоящего Федерального закона, и граждан, признанных в установленном законом порядке недееспособными, информация о состоянии здоровья предоставляется их законным представителям.</p>
            <p>3. Информация о состоянии здоровья не может быть предоставлена пациенту против его воли. В случае неблагоприятного прогноза развития заболевания информация должна сообщаться в деликатной форме гражданину или его супругу (супруге), одному из близких родственников (детям, родителям, усыновленным, усыновителям, родным братьям и родным сестрам, внукам, дедушкам, бабушкам), если пациент не запретил сообщать им об этом и (или) не определил иное лицо, которому должна быть передана такая информация.</p>
            <p>4. Пациент либо его законный представитель имеет право непосредственно знакомиться с медицинской документацией, отражающей состояние его здоровья, в порядке, установленном уполномоченным федеральным органом исполнительной власти, и получать на основании такой документации консультации у других специалистов. (в ред. Федерального закона от 25.11.2013 N 317-ФЗ)</p>
            <p>1. Пациент либо его законный представитель имеет право на основании письменного заявления получать отражающие состояние здоровья медицинские документы, их копии и выписки из медицинских документов. Основания, порядок и сроки предоставления медицинских документов (их копий) и выписок из них устанавливаются уполномоченным федеральным органом исполнительной власти.</p>
            <p>Статья 23. Информация о факторах, влияющих на здоровье</p>
            <p>Граждане имеют право на получение достоверной и своевременной информации о факторах, способствующих сохранению здоровья или оказывающих на него вредное влияние, включая информацию о санитарно-эпидемиологическом благополучии района проживания, состоянии среды обитания, рациональных нормах питания, качестве и безопасности продукции производственно-технического назначения, пищевых продуктов, товаров для личных и бытовых нужд, потенциальной опасности для здоровья человека выполняемых работ и оказываемых услуг. Такая информация предоставляется органами государственной власти и органами местного самоуправления в соответствии с их полномочиями, а также организациями в порядке, предусмотренном законодательством Российской Федерации.</p>
            <p>Статья 24. Права работников, занятых на отдельных видах работ, на охрану здоровья</p>
            <p>1. В целях охраны здоровья и сохранения способности к труду, предупреждения и своевременного выявления профессиональных заболеваний работники, занятые на работах с вредными и (или) опасными производственными факторами, а также в случаях, предусмотренных законодательством Российской Федерации, работники, занятые на отдельных видах работ, проходят обязательные медицинские осмотры.</p>
            <p>2. Перечень вредных и (или) опасных производственных факторов и работ, при выполнении которых проводятся обязательные предварительные медицинские осмотры при поступлении на работу и периодические медицинские осмотры, утверждается уполномоченным федеральным органом исполнительной власти.</p>
            <p>3. В случае выявления при проведении обязательных медицинских осмотров медицинских противопоказаний к осуществлению отдельных видов работ, перечень которых устанавливается уполномоченным федеральным органом исполнительной власти, работник может быть признан врачебной комиссией медицинской организации на основании результатов экспертизы профессиональной пригодности временно или постоянно непригодным по состоянию здоровья к выполнению отдельных видов работ.</p>
            <p>4. В целях охраны здоровья работодатели вправе вводить в штат должности медицинских работников и создавать подразделения (кабинет врача, здравпункт, медицинский кабинет, медицинскую часть и другие подразделения), оказывающие медицинскую помощь работникам организации. Порядок организации деятельности таких подразделений и медицинских работников устанавливается уполномоченным федеральным органом исполнительной власти.</p>
            <p>5. Работодатели обязаны обеспечивать условия для прохождения работниками медицинских осмотров и диспансеризации, а также беспрепятственно отпускать работников для их прохождения.</p>
            <p>Статья 25. Права военнослужащих и лиц, приравненных по медицинскому обеспечению к военнослужащим, а также граждан, проходящих альтернативную гражданскую службу, граждан, подлежащих призыву на военную службу (направляемых на альтернативную гражданскую службу), и граждан, поступающих на военную службу или приравненную к ней службу по контракту, на охрану здоровья</p>
            <p>1. Военнослужащие и лица, приравненные по медицинскому обеспечению к военнослужащим (далее — военнослужащие и приравненные к ним лица), а также граждане, проходящие альтернативную гражданскую службу, имеют право на прохождение военно-врачебной экспертизы для определения годности к военной службе или приравненной к ней службе и для досрочного увольнения с военной службы или приравненной к ней службы на основании заключения военно-врачебной комиссии.</p>
            <p>2. Граждане, подлежащие призыву на военную службу или направляемые на альтернативную гражданскую службу, и граждане, поступающие на военную службу или приравненную к ней службу по контракту, проходят медицинское освидетельствование в порядке, предусмотренном статьей 61 настоящего Федерального закона, и имеют право на получение полной информации о медицинских противопоказаниях для прохождения военной службы или приравненной к ней службы и показаниях для отсрочки или освобождения от призыва на военную службу по состоянию здоровья.</p>
            <p>3. Военнослужащие и приравненные к ним лица имеют право на получение медицинской помощи в ведомственных медицинских организациях, а при их отсутствии или при отсутствии в ведомственных медицинских организациях отделений соответствующего профиля, специалистов либо специального медицинского оборудования — на получение медицинской помощи в порядке, установленном Правительством Российской Федерации, за счет бюджетных ассигнований федерального бюджета, предусмотренных на эти цели федеральным органам исполнительной власти и федеральным государственным органам, в которых федеральным законом предусмотрена военная служба или приравненная к ней служба. (в ред. Федерального закона от 04.06.2014 N 145-ФЗ)</p>
            <p>1. Порядок организации медицинской помощи военнослужащим и приравненным к ним лицам устанавливается Правительством Российской Федерации, особенности организации оказания медицинской помощи военнослужащим и приравненным к ним лицам, в том числе порядок их освобождения от исполнения обязанностей военной службы (служебных обязанностей) в связи с заболеванием и иными причинами, устанавливаются федеральными органами исполнительной власти и федеральными государственными органами, в которых федеральным законом предусмотрена военная служба или приравненная к ней служба. (в ред. Федерального закона от 04.06.2014 N 145-ФЗ)</p>
            <p>1. Граждане при постановке их на воинский учет, призыве или поступлении на военную службу по контракту или приравненную к ней службу, поступлении в военные профессиональные образовательные организации или военные образовательные организации высшего образования, заключении с Министерством обороны Российской Федерации договора об обучении в учебном военном центре при федеральной государственной образовательной организации высшего образования по программе военной подготовки для прохождения военной службы по контракту на воинских должностях, подлежащих замещению офицерами, или на военной кафедре при федеральной государственной образовательной организации высшего образования по программе военной подготовки офицеров запаса, программе военной подготовки сержантов, старшин запаса либо программе военной подготовки солдат, матросов запаса, или в военной образовательной организации высшего образования по программе военной подготовки сержантов, старшин запаса либо программе военной подготовки солдат, матросов запаса, призыве на военные сборы, а также граждане, направляемые на альтернативную гражданскую службу, имеют право на получение медицинской помощи в медицинских организациях в рамках программы государственных гарантий бесплатного оказания гражданам медицинской помощи, за исключением медицинского освидетельствования в целях определения годности к военной службе или приравненной к ней службе. (часть 5 в ред. Федерального закона от 03.04.2017 N 61-ФЗ)</p>
            <p>1. Особенности охраны здоровья военнослужащих и приравненных к ним лиц, а также отдельных категорий граждан, проходящих военную службу или приравненную к ней службу в федеральных органах исполнительной власти и федеральных государственных органах, в которых федеральным законом предусмотрена военная служба или приравненная к ней служба, определяются законодательством Российской Федерации, регламентирующим деятельность этих органов. (в ред. Федерального закона от 04.06.2014 N 145-ФЗ)</p>
            <p>Статья 26. Права лиц, задержанных, заключенных под стражу, отбывающих наказание в виде ограничения свободы, ареста, лишения свободы либо административного ареста, на получение медицинской помощи</p>
            <p>1. Лица, задержанные, заключенные под стражу, отбывающие наказание в виде ограничения свободы, ареста, лишения свободы либо административного ареста, имеют право на оказание медицинской помощи, в том числе в необходимых случаях в медицинских организациях государственной системы здравоохранения и муниципальной системы здравоохранения, в соответствии с законодательством Российской Федерации.</p>
            <p>2. Беременные женщины, женщины во время родов и в послеродовой период из числа лиц, указанных в части 1 настоящей статьи, имеют право на оказание медицинской помощи, в том числе в медицинских организациях охраны материнства и детства.</p>
            <p>3. При невозможности оказания медицинской помощи в учреждениях уголовно-исполнительной системы лица, заключенные под стражу или отбывающие наказание в виде лишения свободы, имеют право на оказание медицинской помощи в медицинских организациях государственной системы здравоохранения и муниципальной системы здравоохранения, а также на приглашение для проведения консультаций врачей-специалистов указанных медицинских организаций в порядке, установленном Правительством Российской Федерации, за счет бюджетных ассигнований федерального бюджета, предусмотренных на эти цели федеральному органу исполнительной власти, осуществляющему правоприменительные функции, функции по контролю и надзору в сфере исполнения уголовных наказаний в отношении осужденных.</p>
            <p>4. При оказании медицинской помощи в медицинских организациях государственной системы здравоохранения и муниципальной системы здравоохранения сотрудниками органов и учреждений уголовно-исполнительной системы осуществляется охрана лиц, указанных в части 3 настоящей статьи, и при необходимости круглосуточное наблюдение в целях обеспечения безопасности указанных лиц, медицинских работников, а также иных лиц, находящихся в медицинских организациях государственной и муниципальной систем здравоохранения, в порядке, установленном федеральным органом исполнительной власти, осуществляющим функции по выработке и реализации государственной политики и нормативно-правовому регулированию в сфере исполнения уголовных наказаний, совместно с уполномоченным федеральным органом исполнительной власти.</p>
            <p>5. Клиническая апробация, испытание лекарственных препаратов, специализированных продуктов лечебного питания, медицинских изделий и дезинфекционных средств с привлечением в качестве объекта для этих целей лиц, указанных в части 1 настоящей статьи, не допускаются. (в ред. Федерального закона от 08.03.2015 N 55-ФЗ)</p>
            <p>1. В отношении лиц, отбывающих наказание в учреждениях уголовно-исполнительной системы, договор о добровольном медицинском страховании расторгается.</p>
            <p>2. Порядок организации оказания медицинской помощи, в том числе в медицинских организациях государственной и муниципальной систем здравоохранения, лицам, указанным в части 1 настоящей статьи, устанавливается законодательством Российской Федерации, в том числе нормативными правовыми актами уполномоченного федерального органа исполнительной власти, осуществляющего функции по выработке и реализации государственной политики и нормативно-правовому регулированию в сфере исполнения уголовных наказаний, по согласованию с уполномоченным федеральным органом исполнительной власти.</p>
            <p>Статья 27. Обязанности граждан в сфере охраны здоровья</p>
            <p>1. Граждане обязаны заботиться о сохранении своего здоровья</p>
            <p>2. Граждане в случаях, предусмотренных законодательством Российской Федерации, обязаны проходить медицинские осмотры, а граждане, страдающие заболеваниями, представляющими опасность для окружающих, в случаях, предусмотренных законодательством Российской Федерации, обязаны проходить медицинское обследование и лечение, а также заниматься профилактикой этих заболеваний.</p>
            <p>3. Граждане, находящиеся на лечении, обязаны соблюдать режим лечения, в том числе определенный на период их временной нетрудоспособности, и правила поведения пациента в медицинских организациях.</p>
            <p>Статья 28. Общественные объединения по защите прав граждан в сфере охраны здоровья</p>
            <p>1. Граждане имеют право на создание общественных объединений по защите прав граждан в сфере охраны здоровья, формируемых на добровольной основе.</p>
            <p>2. Общественные объединения по защите прав граждан в сфере охраны здоровья могут в установленном законодательством Российской Федерации порядке принимать участие в разработке норм и правил в сфере охраны здоровья и решении вопросов, связанных с нарушением таких норм и правил.</p>
            <p>3. Общественные объединения по защите прав граждан в сфере охраны здоровья не вправе осуществлять рекламу конкретных торговых наименований лекарственных препаратов, биологически активных добавок, медицинских изделий, специализированных продуктов лечебного питания и заменителей грудного молока.</p>
